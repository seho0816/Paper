def build_rag_prompt(code: str, rag_context: str, mitre_context: str) -> str:
    """
    RAG 버전 — 취약 코드 분석.
    rag_context, mitre_context 가 비어 있으면 호출하지 말 것.
    """
    return f"""당신은 파이썬 보안 코드 분석 전문가입니다.
아래 [참고 지식]을 바탕으로 [분석 대상 코드]의 취약점을 분석하세요.

[핵심 지시사항]
1. 복붙 금지: DB 예제 코드를 그대로 복사하지 마세요.
2. 무관함 판단: 참고 지식과 관련이 없으면 억지로 찾지 말고 취약점 없음으로 판단하세요.
3. 정확한 식별: 가장 직접적인 원인 하나를 최종 CWE로 선택하세요.
4. 양식 준수: 반드시 아래 [출력 템플릿]을 100% 그대로 따르세요.

{_CWE_RULES}

**[MITRE 공식 CWE 기준]**
{mitre_context}

**[Security Knowledge Base]**
{rag_context}

[분석 대상 코드]
{code}

{_OUTPUT_TEMPLATE}"""


def build_raw_prompt(code: str) -> str:
    """
    Raw 버전 — 취약 코드 분석 (RAG 없음).
    구조는 RAG 버전과 동일, [참고 지식] 블록만 없음.
    """
    return f"""당신은 파이썬 보안 코드 분석 전문가입니다.
아래 [분석 대상 코드]의 취약점을 당신의 자체 지식만으로 분석하세요.

[핵심 지시사항]
1. 취약점이 없다고 판단되면 억지로 찾지 말고 취약점 없음으로 판단하세요.
2. 가장 직접적인 원인 하나를 최종 CWE로 선택하세요.
3. 반드시 아래 [출력 템플릿]을 100% 그대로 따르세요.

{_CWE_RULES}

[분석 대상 코드]
{code}

{_OUTPUT_TEMPLATE}"""


def build_safe_check_prompt(code: str, use_rag: bool = False,
                             rag_context: str = "", mitre_context: str = "") -> str:
    """
    패치 코드 전용 — 보안 안전성 검사 프롬프트.

    [설계 이유]
    패치된 코드를 취약 코드와 동일한 프롬프트로 분석하면
    구조가 유사해 RAG가 취약 패턴을 찾아 CWE를 예측해버림.
    이 프롬프트는 "이 코드가 이미 안전하게 작성되었을 수 있다"는
    가능성을 명시적으로 제시하여 모델이 None을 출력할 여지를 줌.

    use_rag=True이면 RAG 컨텍스트도 포함 (RAG 버전 패치 파일 평가용).
    """
    knowledge_block = ""
    if use_rag and rag_context:
        knowledge_block = f"""
[참고 지식 - 보안 패턴 비교용]
아래 지식은 취약한 패턴과 안전한 패턴을 함께 담고 있습니다.
코드가 이미 안전한 패턴을 따르고 있다면 취약점 없음으로 판단하세요.

**[MITRE 공식 CWE 기준]**
{mitre_context}

**[Security Knowledge Base]**
{rag_context}
"""

    return f"""당신은 파이썬 보안 코드 검토 전문가입니다.
아래 [검토 대상 코드]의 보안 상태를 분석하세요.

[핵심 지시사항]
1. 이 코드는 이미 보안 패치가 적용된 코드일 수 있습니다.
2. 실제 취약점이 존재하는지 면밀히 검토하고, 안전하다면 반드시 None으로 판단하세요.
3. 억지로 취약점을 찾지 마세요. 안전한 코드를 취약하다고 오진하는 것은 오탐(FP)입니다.
4. 반드시 아래 [출력 템플릿]을 100% 그대로 따르세요.
{knowledge_block}
[검토 대상 코드]
{code}

{_SAFE_OUTPUT_TEMPLATE}"""
